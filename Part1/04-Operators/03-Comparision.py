# < Less than > Greater than <= Less than or equal >= Greater or equal == Equal != not Equal

x = 20
y = 33

if x < y:
    print('True')
else:
    print('False')

if x > y:
    print('True')
else:
    print('False')

if x <= y:
    print('True')
else:
    print('False')

if x >= y:
    print('True')
else:
    print('False')

if x == y:
    print('True')
else:
    print('False')

if x != y:
    print('True')
else:
    print('False')
